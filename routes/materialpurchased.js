app.get('/TabMaterialRequest', (req, res, next) => {
  db.query(`SELECT mr.materials_request_id,mr.mr_code FROM materials_request mr WHERE mr.project_id != '' ORDER BY mr.materials_request_id DESC`,
    (err, result) => {
       
      if (result.length == 0) {
        return res.status(400).send({
          msg: 'No result found'
        });
      } else {
            return res.status(200).send({
              data: result,
              msg:'Success'
            });

        }
 
    }
  );
});

app.get('/TabMaterialRequestlineitems', (req, res, next) => {
  db.query(`SELECT mrli.*,s.company_name FROM materials_request_line_items mrli LEFT JOIN supplier s ON (s.supplier_id = mrli.supplier_id) WHERE mrli.materials_request_id != '' ORDER BY mrli.item_title ASC`,
    (err, result) => {
       
      if (result.length == 0) {
        return res.status(400).send({
          msg: 'No result found'
        });
      } else {
            return res.status(200).send({
              data: result,
              msg:'Success'
            });

        }
 
    }
  );
});


